# seprator & end

# syntax : sep = "$" 
# syntax : end = "@gmail.com"

# "char u want as seprator or to end line with" 

print("H","M",sep = "&") # H&M

print("xyz",end = "@gmail.com") # xyz@gmail.com

print('py','thon',sep="-",end=".org") # py-thon.org

print('Py',10) # Py 10
print('Py',10,sep="") # no space : Py10 (concat)