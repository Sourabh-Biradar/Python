# Time package
# Date-Time

import time

yr = time.strftime("%Y")
print(yr)

t = time.strftime
print("Time :",t("%H:%M:%S"))

# strftime : str format time

# %y: Year without century (e.g., 24)

# %m: Month (e.g., 01 for January)
# %B: Full month name (e.g., January)
# %b: Abbreviated month name (e.g., Jan)

# %d: Day of the month (e.g., 05)
# %A: Full weekday name (e.g., Monday)
# %a: Abbreviated weekday name (e.g., Mon)

# %H: Hour (00-23)
# %I: Hour (01-12)
# %p: AM or PM
# %M: Minute (00-59)
# %S: Second (00-59)


